#Ödev1-1: Değişkenlere atanmış değerlerin veri tipleri arasında dönüşüm yapılır.
int_var = 10        # Integer
float_var = 20.5    # Float
str_var = "30"      # String

float_to_int = int(float_var)    
int_to_str = str(int_var)      
str_to_float = float(str_var)    


print("Float to Int:", float_to_int)
print("Int to Str:", int_to_str)
print("Str to Float:",str_to_float)





